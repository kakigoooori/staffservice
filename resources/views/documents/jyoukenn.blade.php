当該労働者に適用される具体的な条件を明示する。また、変形労働時間制、フレックスタイム制、裁量労
働制等の適用がある場合には、次に留意して記載する。
○変形労働時間制：適用する変形労働時間制の種類（１年単位、１か月単位等）を記載する。その際、
 交替制でない場合、「・交替制」を＝＝ で抹消しておく。
○交替制：シフト毎の始業・終業の時刻を記載すること。また、変形労働時間制でない場合、
 「（ ）単位の変形労働時間制・」を＝＝ で抹消しておく。
○フレックスタイム制：コアタイムまたはフレキシブルタイムがある場合はその時間帯の開始および終了の
時刻を記載する。コアタイム、フレキシブルタイムがない場合、かっこ書きを＝＝で抹消しておく。
○事業場外みなし労働時間制：所定の始業および終業の時刻を記載する。
○裁量労働制：基本とする始業・終業時刻がない場合、「始業 ··········を基本とし、」の部分を＝＝で
 抹消しておく。
４．休日および勤務日 所定休日または勤務日について曜日または日を特定して記載する。
５．休暇
年次有給休暇は６か月間勤続勤務し、その間の出勤率が８割以上であるときに与えるものであり、その付与
日数を記載する。時間単位年休は、労使協定を締結し、時間単位の年次有給休暇を付与するものであり、その
制度の有無を記載する。代替休暇は、労使協定を締結し、法定超えとなる所定時間外労働が１か月６０時間を
超える場合に、法定割増賃金率の引上げ分の割増賃金の支払に代えて有給の休暇を与えるものであり、その制
度の有無を記載する。（中小事業主を除く）
また、その他の休暇については、制度がある場合に有給、無給別に休暇の種類、日数（期間など）を記載す
る。
前記３、４および５については、明示すべき事項の内容が膨大なものとなる場合においては、「所定時間外労働の有無」以外の事項に
ついては、勤務の種類ごとの始業および終業の時刻、休日等に関する考え方を示した上、当該労働者に適用される就業規則上の関係条項
名を網羅的に示すことでよい。
