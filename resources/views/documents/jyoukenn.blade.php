【記載要領】
１．労働条件通知書は、当該労働者の労働条件の決定について権限をもつ者が作成し、本人に交付してください。
２．各欄において複数項目の一つを選択する場合には、該当項目に○をつけてください。
３．下線部、破線内および二重線内の事項以外の事項は、書面の交付により明示することが労働基準法により義務付けられている事項です。ま
た、退職金に関する事項、臨時に支払われる賃金等に関する事項、労働者に負担させるべきものに関する事項、安全・衛生に関する事項、職
業訓練に関する事項、災害補償および業務外の傷病扶助に関する事項、表彰・制裁に関する事項、休職に関する事項については、当該事項を
制度として設けている場合には口頭または書面により明示する義務があります。
１．契約期間 労働基準法に定める範囲内とする。また、「契約期間」について「期間の定めあり」とした場合には、契約
の更新の有無および更新する場合、またはしない場合の判断の基準（複数可）を明示する。
