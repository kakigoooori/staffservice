○法定超えとなる所定時間外労働については２割５分、法定超えとなる所定時間外労働が１か月６０時間を超
える場合については５割（中小事業主を除く）、法定休日労働については３割５分、深夜労働については２割
５分、法定超えとなる所定時間外労働が深夜労働となる場合については５割、法定超えとなる所定時間外労
働が１か月６０時間を超え、かつ、深夜労働となる場合については７割５分（中小事業主を除く）、法定休日
労働が深夜労働となる場合については６割を超える割増率とする。
